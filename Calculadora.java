public class Calculadora{ 

  public int Sumar(int num1, int num2){
    int sumar=0;
    sumar = num1 + num2;
    return sumar;
  }
  
  public int Restar(int num1, int num2){
    int restar=0;
    restar = num1 - num2;
    return restar;
  }
  
  public int Multiplicar(int num1, int num2){
    int multiplicar =0;
    multiplicar = num1 * num2;
    return multiplicar;
  }

    public int Dividir(int num1, int num2){
    int dividir =0;
    dividir = num1 / num2;
    return dividir;
  } 
  
}